#include <iostream>
using namespace std;


int main()
{
    int i=1, rows;
    cout<<"Enter the number of rows: ";
    cin>>rows;
    
    while (i<=rows){
    	
    	int j=1;

    	
    	while (j<=i){
    		
    		cout<<"* " <<endl;
    		
    	}
    	j++;
    }
    i++;
    
    return 0;
    

}
